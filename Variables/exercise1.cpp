// Create a program that calculates the area and perimeter of a rectangle using user-provided length and width values.

#include <iostream>
using namespace std;
int length = 23;
int width = 5;




int main()
{
    cout <<"Area of a rectangle: " <<length * width << endl;
    cout <<"Perimeter of a rectangle: "<< 2*(length + width) << endl;


   
    return 0;
}

